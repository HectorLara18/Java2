public class TiposPrimitivos {
    public static void main(String[] args) {
        byte numero1 = 12;
        System.out.println("numero1 = " + numero1);

        char myCharUni =  '\u0021'; //sirve para guardar valores en unicode y te regresa el caracter 0021 -> !
        char myCharDec = 33;
        char myCharSym = '!';
        int letraA = 'A';
        
        System.out.println("myCharUni = " + myCharUni);
        System.out.println("myCharDec = " + myCharDec);
        System.out.println("myCharSym = " + myCharSym);
        System.out.println("letraA = " + letraA);

    }
}
